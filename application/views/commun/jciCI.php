		Le choix du nom de l’OLM :

		Avec un bureau formé, il a fallu s’identifier par rapport à l’OLM existante d’Abidjan. Il s’agit de trouver un nom pour cette nouvelle OLM. Le nom Abidjan (parce que l’OLM est à Abidjan) Ivoire (matière précieuse et résistante symbole pour notre pays) a été donné et adopté par tous.


		Les modifications survenues :

		Un évènement majeur allait se produire au sein de l’équipe dirigeante provisoire. Le Président HALLAUSER Alfred a été muté au Sénégal. Ce départ a occasionné un réaménagement à la tête du bureau provisoire : l’ami YEO Sékou Charles est passé Président et un nouveau membre a fait son entrée pour occuper le poste de VPE (DIENG Abdoulaye). Jusqu’alors l’effectif ne dépassait pas 15 membres.

		Mais sous la poussée du Sénateur KROU Ettien et de ses amis, des jeunes sympathisants ont continué de rallier les rangs de l’OLM naissante. Le 26 mai 1988, quelques jours avant le Conseil National de Korhogo dans la même salle de réunion. Un nombre record de 31 membres a été atteint. Le bureau a été complété avec les amis MAH Diakité (Relations publiques). GADEAU Jean Charles et TAYLOR Samuel (comité des finances).
