    	II.   Jeune Chambre Internationale Côte d’Ivoire 
		1. Chambre Internationale Côte d'Ivoire
		La Jeune Chambre Internationale de Côte d’Ivoire a été créée en 1957 sur l’initiative de quelques Européens désireux de partager avec des jeunes Africains, l’enthousiasme et la fierté d’appartenir à une communauté débarrassée de tout complexe racial ou religieux, attachés avant tout au perfectionnement de ses membres et à la recherche du mieux-être pour tous.

		L’association fut agréée par les Autorités de côte d’Ivoire en 1961.

		C’est en 1963, que la délégation de la Jeune Chambre de Côte d’Ivoire, conduite par Messieurs TADJO EHUE Joseph et Abou DOUMBIA, obtient lors du Congrès Mondial de Tel Aviv (Israël), par acclamation, l’affiliation de la Jeune Chambre Economique de Côte d’Ivoire à l’organisation Jeune Chambre Internationale.

		2. Jeune Chambre Internationale Abidjan Ivoire 
		Les premiers contacts :

		La plupart des premiers contacts ont eu lieu avec le Sénateur KROU Ettien qui a demandé à des jeunes gens âgés de 18 à 40 ans intéressés par la Jeune Chambre Internationale de former une deuxième organisation locale membre à Abidjan. Les rencontres ont été multipliées entre ces jeunes qui ont fini par se retrouver une fois par semaine au nombre de 15 au départ.


		Comme ce genre de rencontre doit être formalisée par un minimum d’infrastructures. Une salle a été mise à leur disposition au siège par la volonté des Sénateurs KROU Ettien et Pierre KANGAH.

		Cette salle est aujourd’hui le siège d’Abidjan Ivoire.


		Le bureau provisoire de l’OLM :

