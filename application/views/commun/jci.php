                <h1>Jeune Chambre Internationale </h1>
                <h3>I.1.1 Historique</h3>

                <p>Les origines de la Jeune Chambre remontent jusqu’en 1910, dans la ville de Saint Louis dans le Missouri aux Etats-Unis. Un jeune homme nommé Henry Giessembier et ses amis forment le Herculaneum Dance Club avec pour objectif principal de conserver les styles de danse traditionnelle.</p><br>

                    <p>Cinq années plus tard, en 1915, le Colonel H.N Morgan, une éminente personnalité de la ville de Saint Louis, encourage les membres de Dance Club à s’intéresser davantage à des problèmes civiques.</p>

                    <p>En 1916, l’association change de nom pour s’appeler « Jeunes Citoyens », plus communément appelée JC qui devient plus tard JAYCEES.</p>

                    <p>Les Jaycees s’affilient en 1918 à la Chambre de Commerce de Saint Louis et deviennent officiellement la Jeune Chambre de Commerce de Saint Louis. Plus tard, la Jeune Chambre de Commerce de Saint Louis prend contact avec des groupes de jeunes Hommes d’Affaires similaires existants dans les Etats-Unis pour former un rassemblement qui débouchera sur la Jeune Chambre de Commerce des Etats-Unis.</p>

                    <p>Grâce à la persévérance de ses membres, cette organisation va évoluer pour atteindre une dimension internationale. Ainsi :</p> <br>

                - En 1944, se tient la première conférence internationale à Mexico ; le premier président mondial y est élu (Raoul VIDAL GARCIA) ; <br>

                - En 1946, le premier congrès mondial se tient à Panama City ;<br>

                - En 1948, le credo de la Jeune Chambre Internationale écrit par William BROWFIELD est adopté. <br><br>

                <p>Aujourd’hui, la Jeune Chambre Internationale est présente dans une centaine de pays à travers le monde, avec près de 200.000 membres répartis dans environ 5.000 organisations locales.</p>

                <h3>I.1.2 Interprétation du credo</h3><br>
                « Nous croyons»

                <p>Chacun doit croire à un idéal, à un principe ou à une philosophie particulière. Croire, signifie mettre en application ce que l’on croit être vrai.</p>



                ·   Que la foi en Dieu donne à la vie son véritable sens… <br>

                <p>Dans cette phrase, « Dieu » ne fait référence à aucun Dieu spécifique, mais se rapporte à une puissance suprême omnipotente. Peu importe qui est – ou ce que représente – votre Dieu : cette phrase signifie que vous devez croire à quelque chose.</p>

                ·   Que la fraternité humaine transcende la souveraineté des nations <br>

               <p> Cette phrase abolit toutes les frontières qui ont été imaginés par l’Homme. Elle signifie simplement que les hommes et les femmes sont égaux. Elle respecte le sentiment de loyauté d’un individu à l’égard de son pays, mais en même temps elle renforce l’idée que l’être humain est un citoyen du monde. </p>

                ·   Que la liberté des individus et de l’entreprise assure mieux la justice économique <br>

                <p>Les mots clés sont ici « assure mieux» Les membres de la Jeune Chambre croient que l’Homme doit être libre d’utiliser ses compétences et capacités à leur maximum pour mieux améliorer sa situation économique. Brownfield exprima cette idée ainsi : « Partout où l’avancement économique a été le plus grand, l’homme a été libre de poursuivre son rêve de faire fortune, en faisant quelque chose de nouveau, ou en perfectionnant des idées anciennes. » il ajouta aussi, « …le système d’auto-développement grâce à l’entreprise privée doit pouvoir être modifié afin de s’adapter aux conditions locales rencontrées dans diverses parties du monde. » </p>

                ·   Que l’autorité doit s’appuyer sur la loi et non sur l’arbitraire… <br>

               <p> Ce principe du credo signifie simplement que personne n’est au-dessus de la loi et que la loi doit être la même pour tous les individus, indépendamment du statut particulier d’une personne au sein de la société. Le gouvernement doit être basé sur des lois constitutionnelles acceptées et ratifiées par la majorité des citoyens. Le pouvoir de changer les lois et d’élire des gouvernements doit rester entre les mains de la majorité des citoyens. Abraham Lincoln, 16e Président des Etats-Unis faisait référence à un gouvernement « du peuple, pour le peuple, par le peuple ». Cette phrase du credo cristallise ce que proclamait le Président Lincoln il y a si longtemps. Brownfield exprima cette idée de la manière suivante : « Dans une société libre, le Droit fondamental doit provenir du peuple. C’est le peuple qui représente le pouvoir suprême ». </p>

                · Que la personne humaine est la plus précieuse des richesses <br>

                <p>Chaque individu possède une personnalité unique qui lui est propre. C’est cela qui différencie l’Homme des autres créatures du monde. Cette particularité reconnaît que l’être humain est le trésor le plus précieux sur la terre. Il ne peut être ni reproduit ni fabriqué. Les vues de Brownfield sur l’interprétation de cette phrase sont : « Le véritable est dans le cœur des Hommes. Il y a en nous, un domaine de possibilités étendues pour développer la personne humaine. » </p>

                ·   Et que servir l’humanité constitue l’œuvre la plus noble d’une vie. <br>

                <p>Ce dernier principe du credo est l’aboutissement logique des lignes précédentes. Une personne qui croit au credo trouvera très certainement que servie l’humanité constitue l’œuvre la plus noble d’une vie. Arrêtez-vous sur le mot humanité. L’interprétation de cette ligne du credo par Brownfield est la suivante « Une vie vécue au service des autres est plus enrichissante, plus profonde et plus complète. Le bonheur est plus durable et la tranquillité d’âme plus certaine. Le monde est conscient de la contribution apportée par une telle vie et reconnaît celui qui l’a vécue comme un bienfaiteur de la race humaine ; cependant ce dernier sait qu’en vérité il est le plus grand bénéficiaire. » </p> <br>

                <p>Peu importe la façon dont un membre interprète le credo, il doit toujours mettre en pratique ce qu’il croit. Plusieurs modifications du credo ont été tentées sans succès, mais le credo a prévalu au cours des années et il continue à être le texte de référence qui maintient la cohésion et l’unité de l’organisation. De nombreux membres ont basé leur ligne de conduite dans la vie sur le credo.</p> <br>

                <p>Brownfield résuma très justement son interprétation du credo de la manière suivante : « seulement dans l’action, les mots peuvent devenir réalité. »</p> <br>


                <h3> I.1.3 La philosophie </h3>
                <h4> Créer un Monde Différent</h4>

                <p>Il y a une limite à ce que peuvent réaliser les gouvernements dans nos sociétés. En tant que citoyens responsables d’un environnement mondialisé, les membres JCI relèvent les défis autour d’eux à travers des initiatives de développement locales. Ces projets faits sur mesure obligent les membres à faire usage des planifications stratégiques et décisives pour concevoir des solutions créatives aux problèmes de leurs communautés.</p><br>

                <h4> Be Better </h4><br>

                <p>Les membres JCI cherchent toujours à vivre leur slogan, Be Better. Nous réfléchissons de manière critique aux grands défis de notre monde et agissons au nom de nos communautés pour faire partie de la solution. Nous cherchons de meilleures solutions pour construire de meilleures communautés et créer un meilleur avenir.</p><br>

                Les Vrais Citoyens Actifs

                <p>En tant que citoyens, nous avons tous des droits et des devoirs, de même que des objectifs partagés. À travers la citoyenneté active, nous mettons en action notre sens de la responsabilité sociale pour atteindre ces objectifs et faire profiter nos communautés.</p><br>

                <p>Notre fondateur, Henry Giessembier, était un vrai citoyen actif. Il a œuvré pour impliquer les jeunes dans l’éducation civique en 1914 et depuis, les membres JCI suivent son pas.</p><br>

                <p>Un Mouvement de Base avec une Envergure Internationale</p><br>

                <p>À la JCI, l’action est locale, mais nos principes et notre influence sont mondiaux. Les membres comprennent que dans un environnement mondialisé, leurs actions locales aient un écho mondial. Environs 10000 Organisations Locales Membres JCI réglant leurs problèmes dans leurs communautés s’unissent en un mouvement mondial pour créer un impact global. La JCI est reconnue par l'Organisation des Nations Unies (ONU) qui l'associe à ses activités. Elle est présente dans une centaine de pays à travers le monde, avec près de deux cent mille (200 000) membres actuellement.</p><br>


                <p>Une Année pour Diriger</p>

                <p>Avec ce principe de base, la JCI offre aux nouveaux membres des opportunités de leadership chaque année. Une année pour briller. Une année pour faire impact. Une année pour diriger.</p><br>


                <h4> I.1.4 Manifestations internationales</h4>
               <h5> Conférences de Zone JCI</h5>

                <p>Chaque année, les Membres JCI se retrouvent au cours des Conférences Zone JCI afin d’échanger des idées, d’apprendre à maximiser leur impact local et collaborer avec d’autres Organisations Nationales dans leur Zone. Les quatre Conférences de Zone Afrique et Moyen-Orient, Asie et Pacifique, les Amériques et l’Europe offrent aux membres une chance de tirer profit du réseau international de la JCI en renforçant leurs Organisations Nationales.</p>

                <p>Congrès Mondial JCI</p>

                <p>Tous les mois de novembre, les membres à travers le globe se réunissent pour l’événement le plus important de l’année au Congrès Mondial JCI. Au cours de ce forum mondial, les délégués élisent les dirigeants de l’année suivante, rencontrent les Membres JCI de plus de 100 pays, trouvent de nouveaux moyens pour créer un changement positif et amplifient l’impact positif du mouvement JCI.</p>
